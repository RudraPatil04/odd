public class odd{
    public static void main(String[] args) {
        int limit = 20; // Change this to set the limit
        System.out.println("Odd numbers from 1 to " + limit + ":");
        for (int i = 1; i <= limit; i++) {
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}





